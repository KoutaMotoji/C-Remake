#include "sky_bg.h"
#include "manager.h"


CSkyBg::CSkyBg()
{

}

CSkyBg::~CSkyBg()
{

}

//==========================================================================================
//初期化処理
//==========================================================================================
void CSkyBg::Init()
{
	//テクスチャの登録・割り当て

	int nIdx = CManager::GetInstance()->GetTexture()->Regist("data\\TEXTURE\\field.jpg");
	BindTexture(CManager::GetInstance()->GetTexture()->GetAddress(nIdx));

	CObject::SetType(TYPE_3D_FIELD);
	CObject3D::Init();
}

//==========================================================================================
//終了処理
//==========================================================================================
void CSkyBg::Uninit()
{
	CObject3D::Uninit();
}

//==========================================================================================
//更新処理
//==========================================================================================
void CSkyBg::Update()
{
	CObject3D::Update();
}

//==========================================================================================
//描画処理
//==========================================================================================
void CSkyBg::Draw()
{
	CObject3D::Draw();
}


//==========================================================================================
//生成処理
//==========================================================================================
CSkyBg* CSkyBg::Create(D3DXVECTOR3 pos)
{
	CSkyBg* field = new CSkyBg;
	field->SetPolygonParam(pos, FLOOR_SIZE, FLOOR_SIZE);
	field->Init();
	field->SetRot({ 0.0f,0.0f,0.0f });
	return field;
}
